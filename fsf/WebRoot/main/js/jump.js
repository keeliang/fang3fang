

function jumpTo(strID)
{
 for(var i = 1;i<=2;i++)
 {
  var a="a"+i;
  if(i==strID){
  try{document.getElementById(a).style.display="block"}catch(e){};
  }else{
  try{document.getElementById(a).style.display="none"}catch(e){};
  }
 }  
}


